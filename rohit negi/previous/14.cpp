// Printing 10 to 1
#include <iostream>
using namespace std;

int main()
{
    for (int i = 10; i >= 1; i--)
    {
        cout << i << endl;
    }
    return 0;
}
// Alternative method: Iterate through all numbers from 1 to 10 and print in reverse order.
#include <bits/stdc++.h>
using namespace std;

int main()
{
    for (int i = 1; i <= 10; i++)
    {
        cout << 11 - i << endl;
    }
    return 0;
}